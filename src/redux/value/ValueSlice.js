import { createSlice } from '@reduxjs/toolkit';

export const valueSlice = createSlice({
  name: 'myValue',
  initialState: 200,
  reducers: {
    increment(state, action) {
      return state + action.payload;
    },
    decrement(state, action) {
      return state - action.payload;
    },
  },
});

export const { increment, decrement } = valueSlice.actions;
